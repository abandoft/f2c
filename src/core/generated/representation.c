#include "core/generated/private.h"

void f2c_emit_real_representation_support(Buffer *output) {
    f2c_buffer_append(
        output,
        "static inline F2C_UNUSED int32_t f2c_exponent_r4(float value) { "
        "int exponent = 0; if (!isfinite(value)) return INT32_MAX; "
        "(void)frexpf(value, &exponent); return (int32_t)exponent; }\n"
        "static inline F2C_UNUSED int32_t f2c_exponent_r8(double value) { "
        "int exponent = 0; if (!isfinite(value)) return INT32_MAX; "
        "(void)frexp(value, &exponent); return (int32_t)exponent; }\n"
        "static inline F2C_UNUSED float f2c_fraction_r4(float value) { "
        "int exponent = 0; if (!isfinite(value)) return NAN; "
        "return frexpf(value, &exponent); }\n"
        "static inline F2C_UNUSED double f2c_fraction_r8(double value) { "
        "int exponent = 0; if (!isfinite(value)) return NAN; "
        "return frexp(value, &exponent); }\n"
        "static inline F2C_UNUSED float f2c_nearest_r4(float value, double direction) { "
        "if (direction == 0.0) abort(); if (isnan(value) || isnan(direction)) return NAN; "
        "return nextafterf(value, direction > 0.0 ? INFINITY : -INFINITY); }\n"
        "static inline F2C_UNUSED double f2c_nearest_r8(double value, double direction) { "
        "if (direction == 0.0) abort(); if (isnan(value) || isnan(direction)) return NAN; "
        "return nextafter(value, direction > 0.0 ? INFINITY : -INFINITY); }\n"
        "static inline F2C_UNUSED float f2c_rrspacing_r4(float value) { "
        "int exponent = 0; float fraction; if (!isfinite(value)) return NAN; "
        "fraction = frexpf(value, &exponent); return scalbnf(fabsf(fraction), FLT_MANT_DIG); }\n"
        "static inline F2C_UNUSED double f2c_rrspacing_r8(double value) { "
        "int exponent = 0; double fraction; if (!isfinite(value)) return NAN; "
        "fraction = frexp(value, &exponent); return scalbn(fabs(fraction), DBL_MANT_DIG); }\n"
        "static inline F2C_UNUSED float f2c_scale_r4(float value, int64_t power) { "
        "if (!isfinite(value) || value == 0.0f) return value; "
        "if (power > INT_MAX) return copysignf(INFINITY, value); "
        "if (power < INT_MIN) return copysignf(0.0f, value); "
        "return scalbnf(value, (int)power); }\n"
        "static inline F2C_UNUSED double f2c_scale_r8(double value, int64_t power) { "
        "if (!isfinite(value) || value == 0.0) return value; "
        "if (power > INT_MAX) return copysign(INFINITY, value); "
        "if (power < INT_MIN) return copysign(0.0, value); "
        "return scalbn(value, (int)power); }\n"
        "static inline F2C_UNUSED float f2c_set_exponent_r4(float value, int64_t power) { "
        "int exponent = 0; if (!isfinite(value)) return NAN; "
        "return f2c_scale_r4(frexpf(value, &exponent), power); }\n"
        "static inline F2C_UNUSED double f2c_set_exponent_r8(double value, int64_t power) { "
        "int exponent = 0; if (!isfinite(value)) return NAN; "
        "return f2c_scale_r8(frexp(value, &exponent), power); }\n"
        "static inline F2C_UNUSED float f2c_spacing_r4(float value) { "
        "int exponent = 0; if (!isfinite(value)) return NAN; if (value == 0.0f) return FLT_MIN; "
        "(void)frexpf(value, &exponent); exponent -= FLT_MANT_DIG; "
        "if (exponent < FLT_MIN_EXP - 1) exponent = FLT_MIN_EXP - 1; "
        "return scalbnf(1.0f, exponent); }\n"
        "static inline F2C_UNUSED double f2c_spacing_r8(double value) { "
        "int exponent = 0; if (!isfinite(value)) return NAN; if (value == 0.0) return DBL_MIN; "
        "(void)frexp(value, &exponent); exponent -= DBL_MANT_DIG; "
        "if (exponent < DBL_MIN_EXP - 1) exponent = DBL_MIN_EXP - 1; "
        "return scalbn(1.0, exponent); }\n");
}
