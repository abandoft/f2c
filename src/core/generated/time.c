#include "core/generated/private.h"

void f2c_emit_time_intrinsic_support(Buffer *output) {
    f2c_buffer_append(
        output,
        "static inline F2C_UNUSED bool f2c_local_time_copy(time_t value, struct tm *result) {\n"
        "#if defined(_WIN32)\n"
        "    return localtime_s(result, &value) == 0;\n"
        "#elif defined(__unix__) || defined(__APPLE__) || defined(__EMSCRIPTEN__)\n"
        "    return localtime_r(&value, result) != NULL;\n"
        "#else\n"
        "    const struct tm *temporary = localtime(&value);\n"
        "    if (temporary == NULL) return false;\n"
        "    *result = *temporary;\n"
        "    return true;\n"
        "#endif\n"
        "}\n"
        "static inline F2C_UNUSED void f2c_clock_copy_text(char *target, size_t length, "
        "const char *source, size_t source_length) {\n"
        "    const size_t count = length < source_length ? length : source_length;\n"
        "    if (target == NULL) return;\n"
        "    if (count != 0U) memcpy(target, source, count);\n"
        "    if (length > count) memset(target + count, ' ', length - count);\n"
        "}\n"
        "static inline F2C_UNUSED void f2c_clock_digits(char *target, size_t width, "
        "unsigned int value) {\n"
        "    size_t index; for (index = width; index != 0U; --index) { "
        "target[index - 1U] = (char)('0' + value % 10U); value /= 10U; }\n"
        "}\n"
        "static inline F2C_UNUSED int32_t f2c_clock_zone(const struct tm *local, "
        "char result[6]) {\n"
        "    int32_t offset = 0;\n"
        "    memset(result, ' ', 5U); result[5] = '\\0';\n"
        "#if defined(__unix__) || defined(__APPLE__) || defined(__EMSCRIPTEN__)\n"
        "    { char zone[16] = {0}; size_t length = strftime(zone, sizeof(zone), \"%z\", local); "
        "size_t minute = 3U;\n"
        "      if (length >= 5U && (zone[0] == '+' || zone[0] == '-') && "
        "isdigit((unsigned char)zone[1]) && isdigit((unsigned char)zone[2])) {\n"
        "        if (zone[3] == ':') minute = 4U;\n"
        "        if (minute + 1U < length && isdigit((unsigned char)zone[minute]) && "
        "isdigit((unsigned char)zone[minute + 1U])) {\n"
        "          int32_t hours = (int32_t)((zone[1] - '0') * 10 + (zone[2] - '0'));\n"
        "          int32_t minutes = (int32_t)((zone[minute] - '0') * 10 + "
        "(zone[minute + 1U] - '0'));\n"
        "          offset = hours * 60 + minutes; if (zone[0] == '-') offset = -offset;\n"
        "          result[0] = zone[0]; result[1] = zone[1]; result[2] = zone[2];\n"
        "          result[3] = zone[minute]; result[4] = zone[minute + 1U];\n"
        "        }\n"
        "      }\n"
        "    }\n"
        "#else\n"
        "    (void)local;\n"
        "#endif\n"
        "    return offset;\n"
        "}\n");
    f2c_buffer_append(
        output,
        "static inline F2C_UNUSED void f2c_date_and_time(char *date, size_t date_length, "
        "char *clock_time, size_t time_length, char *zone, size_t zone_length, "
        "int64_t values[8]) {\n"
        "    struct timespec instant; struct tm local; char date_value[9] = \"        \"; "
        "char time_value[11] = \"          \"; char zone_value[6]; int32_t offset; "
        "int index; int year;\n"
        "    if (timespec_get(&instant, TIME_UTC) != TIME_UTC) { instant.tv_sec = time(NULL); "
        "instant.tv_nsec = 0; }\n"
        "    if (instant.tv_sec == (time_t)-1 || !f2c_local_time_copy(instant.tv_sec, &local)) {\n"
        "        f2c_clock_copy_text(date, date_length, \"\", 0U);\n"
        "        f2c_clock_copy_text(clock_time, time_length, \"\", 0U);\n"
        "        f2c_clock_copy_text(zone, zone_length, \"\", 0U);\n"
        "        if (values != NULL) for (index = 0; index < 8; ++index) values[index] = "
        "-INT64_MAX;\n"
        "        return;\n"
        "    }\n"
        "    year = local.tm_year + 1900;\n"
        "    if (year >= 0 && year <= 9999) { f2c_clock_digits(date_value, 4U, "
        "(unsigned int)year); f2c_clock_digits(date_value + 4U, 2U, "
        "(unsigned int)(local.tm_mon + 1)); f2c_clock_digits(date_value + 6U, 2U, "
        "(unsigned int)local.tm_mday); }\n"
        "    f2c_clock_digits(time_value, 2U, (unsigned int)local.tm_hour);\n"
        "    f2c_clock_digits(time_value + 2U, 2U, (unsigned int)local.tm_min);\n"
        "    f2c_clock_digits(time_value + 4U, 2U, (unsigned int)local.tm_sec);\n"
        "    time_value[6] = '.'; f2c_clock_digits(time_value + 7U, 3U, "
        "(unsigned int)(instant.tv_nsec / 1000000L));\n"
        "    offset = f2c_clock_zone(&local, zone_value);\n"
        "    f2c_clock_copy_text(date, date_length, date_value, 8U);\n"
        "    f2c_clock_copy_text(clock_time, time_length, time_value, 10U);\n"
        "    f2c_clock_copy_text(zone, zone_length, zone_value, 5U);\n"
        "    if (values != NULL) {\n"
        "        values[0] = (int64_t)local.tm_year + INT64_C(1900);\n"
        "        values[1] = (int64_t)local.tm_mon + INT64_C(1);\n"
        "        values[2] = (int64_t)local.tm_mday; values[3] = (int64_t)offset;\n"
        "        values[4] = (int64_t)local.tm_hour; values[5] = (int64_t)local.tm_min;\n"
        "        values[6] = (int64_t)local.tm_sec;\n"
        "        values[7] = (int64_t)(instant.tv_nsec / 1000000L);\n"
        "    }\n"
        "}\n");
    f2c_buffer_append(
        output,
        "static inline F2C_UNUSED uint64_t f2c_clock_add_mod(uint64_t left, uint64_t right, "
        "uint64_t modulus) { return left >= modulus - right ? left - (modulus - right) : "
        "left + right; }\n"
        "static inline F2C_UNUSED uint64_t f2c_clock_multiply_mod(uint64_t left, uint64_t right, "
        "uint64_t modulus) {\n"
        "    uint64_t result = 0U; left %= modulus;\n"
        "    while (right != 0U) { if ((right & UINT64_C(1)) != 0U) "
        "result = f2c_clock_add_mod(result, left, modulus); right >>= 1; "
        "if (right != 0U) left = f2c_clock_add_mod(left, left, modulus); }\n"
        "    return result;\n"
        "}\n"
        "static inline F2C_UNUSED int64_t f2c_system_clock_count(uint64_t rate, "
        "uint64_t maximum) {\n"
        "    struct timespec instant; uint64_t modulus = maximum + UINT64_C(1); "
        "uint64_t seconds; uint64_t ticks; uint64_t fraction;\n"
        "#if defined(__unix__) || defined(__APPLE__) || defined(__EMSCRIPTEN__)\n"
        "    if (clock_gettime(CLOCK_MONOTONIC, &instant) != 0) return -INT64_MAX;\n"
        "#else\n"
        "    if (timespec_get(&instant, TIME_UTC) != TIME_UTC) return -INT64_MAX;\n"
        "#endif\n"
        "    if (instant.tv_sec < (time_t)0) return -INT64_MAX;\n"
        "    seconds = (uint64_t)instant.tv_sec;\n"
        "    ticks = f2c_clock_multiply_mod(seconds, rate, modulus);\n"
        "    fraction = ((uint64_t)instant.tv_nsec * rate) / UINT64_C(1000000000);\n"
        "    ticks = f2c_clock_add_mod(ticks, fraction % modulus, modulus);\n"
        "    return (int64_t)ticks;\n"
        "}\n"
        "static inline F2C_UNUSED double f2c_cpu_time(void) {\n"
        "    const clock_t value = clock();\n"
        "    return value == (clock_t)-1 ? -1.0 : (double)value / (double)CLOCKS_PER_SEC;\n"
        "}\n");
}
