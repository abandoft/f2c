#include "core/generated/private.h"

void f2c_emit_character_intrinsic_support(Buffer *output) {
    f2c_buffer_append(
        output,
        "static inline F2C_UNUSED int64_t f2c_character_position(size_t value, int kind) { "
        "uint64_t maximum = kind == 1 ? UINT64_C(127) : kind == 2 ? UINT64_C(32767) : kind "
        "== 4 ? UINT64_C(2147483647) : kind == 8 ? (uint64_t)INT64_MAX : UINT64_C(0); if "
        "(maximum == 0U || (uint64_t)value > maximum) abort(); return (int64_t)value; }\n"
        "static inline F2C_UNUSED unsigned char f2c_character_code(int64_t value) { if "
        "(value < 0 || value > INT64_C(255)) abort(); return (unsigned char)value; }\n"
        "static inline F2C_UNUSED size_t f2c_character_code_value(const char *value, size_t "
        "length) { if (value == NULL || length != 1U) abort(); return (unsigned char)value[0]; "
        "}\n"
        "static inline F2C_UNUSED size_t f2c_character_repeat_length(size_t length, int64_t "
        "count) { if (count < 0 || (length != 0U && (uint64_t)count > "
        "(uint64_t)SIZE_MAX / length)) abort(); return length * (size_t)count; }\n");
    f2c_buffer_append(
        output,
        "static inline F2C_UNUSED size_t f2c_character_intrinsic_trim_length(const char "
        "*value, size_t length) { if (value == NULL && length != 0U) abort(); while (length != "
        "0U && value[length - 1U] == ' ') --length; return length; }\n"
        "static inline F2C_UNUSED char *f2c_character_intrinsic_resize(char *storage, size_t "
        "length) { char *replacement; if (length == SIZE_MAX) abort(); replacement = (char "
        "*)realloc(storage, length + 1U); if (replacement == NULL) abort(); return replacement; "
        "}\n"
        "static inline F2C_UNUSED char *f2c_character_adjust(char **storage, const char "
        "*source, size_t length, bool right) { size_t blanks = 0U; int same; if (storage == "
        "NULL || (source == NULL && length != 0U)) abort(); same = source == *storage; "
        "*storage = f2c_character_intrinsic_resize(*storage, length); if (same) source = "
        "*storage; if (right) { while (blanks < length && source[length - blanks - 1U] == ' ') "
        "++blanks; if (length > blanks) memmove(*storage + blanks, source, length - blanks); if "
        "(blanks != 0U) memset(*storage, ' ', blanks); } else { while (blanks < length && "
        "source[blanks] == ' ') ++blanks; if (length > blanks) memmove(*storage, source + "
        "blanks, length - blanks); if (blanks != 0U) memset(*storage + length - blanks, ' ', "
        "blanks); } (*storage)[length] = '\\0'; return *storage; }\n"
        "static inline F2C_UNUSED char *f2c_character_repeat(char **storage, const char "
        "*source, size_t length, int64_t count) { size_t result_length = "
        "f2c_character_repeat_length(length, count); size_t copy; int same; if (storage == NULL "
        "|| (source == NULL && length != 0U)) abort(); same = source == *storage; *storage = "
        "f2c_character_intrinsic_resize(*storage, result_length); if (same) source = *storage; "
        "for (copy = 0U; copy < (size_t)count; ++copy) if (length != 0U) memmove(*storage + "
        "copy * length, source, length); (*storage)[result_length] = '\\0'; return *storage; }\n"
        "static inline F2C_UNUSED char *f2c_character_trim(char **storage, const char *source, "
        "size_t length) { size_t result_length; int same; if (storage == NULL || (source == "
        "NULL && length != 0U)) abort(); result_length = "
        "f2c_character_intrinsic_trim_length(source, length); same = source == *storage; *storage "
        "= f2c_character_intrinsic_resize(*storage, "
        "result_length); if (same) source = *storage; if (result_length != 0U) memmove(*storage, "
        "source, result_length); (*storage)[result_length] = '\\0'; return *storage; }\n");
    f2c_buffer_append(
        output,
        "static inline F2C_UNUSED size_t f2c_character_index(const char *string, size_t length, "
        "const char *substring, size_t substring_length, bool back) { size_t position; if "
        "((string == NULL && length != 0U) || (substring == NULL && substring_length != 0U)) "
        "abort(); if (substring_length == 0U) return back ? length + 1U : 1U; if "
        "(substring_length > length) return 0U; if (back) { position = length - "
        "substring_length; for (;;) { if (memcmp(string + position, substring, "
        "substring_length) == 0) return position + 1U; if (position == 0U) break; --position; } "
        "} else { for (position = 0U; position <= length - substring_length; ++position) if "
        "(memcmp(string + position, substring, substring_length) == 0) return position + 1U; } "
        "return 0U; }\n"
        "static inline F2C_UNUSED bool f2c_character_set_member(unsigned char value, const char "
        "*set, size_t length) { size_t index; for (index = 0U; index < length; ++index) if "
        "((unsigned char)set[index] == value) return true; return false; }\n"
        "static inline F2C_UNUSED size_t f2c_character_scan(const char *string, size_t length, "
        "const char *set, size_t set_length, bool back) { size_t position = back ? length : 0U; "
        "if ((string == NULL && length != 0U) || (set == NULL && set_length != 0U)) abort(); if "
        "(back) { while (position != 0U) { if (f2c_character_set_member((unsigned "
        "char)string[position - 1U], set, set_length)) return position; --position; } } else { "
        "for (; position < length; ++position) if (f2c_character_set_member((unsigned "
        "char)string[position], set, set_length)) return position + 1U; } return 0U; }\n"
        "static inline F2C_UNUSED size_t f2c_character_verify(const char *string, size_t "
        "length, const char *set, size_t set_length, bool back) { size_t position = back ? "
        "length : 0U; if ((string == NULL && length != 0U) || (set == NULL && set_length != "
        "0U)) abort(); if (back) { while (position != 0U) { if "
        "(!f2c_character_set_member((unsigned char)string[position - 1U], set, set_length)) "
        "return position; --position; } } else { for (; position < length; ++position) if "
        "(!f2c_character_set_member((unsigned char)string[position], set, set_length)) return "
        "position + 1U; } return 0U; }\n");
}
