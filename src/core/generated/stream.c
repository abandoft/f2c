#include "core/generated/private.h"

void f2c_emit_io_stream_support(Buffer *output) {
    f2c_buffer_append(
        output,
        "typedef enum f2c_io_stream_kind { F2C_IO_STREAM_EXTERNAL, F2C_IO_STREAM_INTERNAL } "
        "f2c_io_stream_kind;\n"
        "typedef struct f2c_io_stream { f2c_io_stream_kind kind; FILE *file; char *records; "
        "size_t record_length; size_t record_count; size_t position; size_t virtual_size; "
        "bool end_of_file; bool error; } f2c_io_stream;\n"
        "static inline F2C_UNUSED void f2c_stream_initialize_external(f2c_io_stream *stream, "
        "FILE *file) { memset(stream, 0, sizeof(*stream)); stream->kind = "
        "F2C_IO_STREAM_EXTERNAL; stream->file = file; }\n"
        "static inline F2C_UNUSED bool f2c_stream_initialize_internal(f2c_io_stream *stream, "
        "char *records, size_t record_length, size_t record_count, bool input) { size_t "
        "stride; memset(stream, 0, sizeof(*stream)); stream->kind = F2C_IO_STREAM_INTERNAL; "
        "stream->records = records; stream->record_length = record_length; "
        "stream->record_count = record_count; if ((records == NULL && record_length != 0U && "
        "record_count != 0U) || record_length == SIZE_MAX) { stream->error = true; return "
        "false; } stride = record_length + 1U; if (record_count > SIZE_MAX / stride) { "
        "stream->error = true; return false; } stream->virtual_size = record_count * stride; "
        "(void)input; return true; }\n");
    f2c_buffer_append(
        output,
        "static inline F2C_UNUSED size_t f2c_stream_record_offset(const f2c_io_stream *stream, "
        "size_t position) { const size_t stride = stream->record_length + 1U; return "
        "(position / stride) * stream->record_length + position % stride; }\n"
        "static inline F2C_UNUSED int f2c_stream_getc(f2c_io_stream *stream) { size_t offset, "
        "position; if (stream == NULL) return EOF; if (stream->kind == F2C_IO_STREAM_EXTERNAL) "
        "return stream->file != NULL ? fgetc(stream->file) : EOF; if (stream->position >= "
        "stream->virtual_size) { stream->end_of_file = true; return EOF; } position = "
        "stream->position++; offset = position % (stream->record_length + 1U); return offset == "
        "stream->record_length ? '\\n' : (unsigned char)stream->records["
        "f2c_stream_record_offset(stream, position)]; }\n"
        "static inline F2C_UNUSED int f2c_stream_putc(int value, f2c_io_stream *stream) { "
        "size_t offset; if (stream == NULL) return EOF; if (stream->kind == "
        "F2C_IO_STREAM_EXTERNAL) return stream->file != NULL ? fputc(value, stream->file) : "
        "EOF; if (stream->position >= stream->virtual_size) { stream->error = true; return "
        "EOF; } offset = stream->position % (stream->record_length + 1U); if (value == '\\n' "
        "|| value == '\\r') { if (offset < stream->record_length && stream->records != NULL) "
        "memset(stream->records + f2c_stream_record_offset(stream, stream->position), ' ', "
        "stream->record_length - offset); stream->position += stream->record_length - offset + "
        "1U; return value; } if (offset == stream->record_length || stream->records == NULL) { "
        "stream->error = true; return EOF; } stream->records[f2c_stream_record_offset(stream, "
        "stream->position)] = (char)value; ++stream->position; return (unsigned char)value; }\n"
        "static inline F2C_UNUSED size_t f2c_stream_read(void *destination, size_t size, "
        "f2c_io_stream *stream) { unsigned char *bytes = (unsigned char *)destination; size_t "
        "count = 0U; if (stream != NULL && stream->kind == F2C_IO_STREAM_EXTERNAL) return "
        "stream->file != NULL ? fread(destination, 1U, size, stream->file) : 0U; while (count < "
        "size) { int value = f2c_stream_getc(stream); if (value == "
        "EOF) break; bytes[count++] = (unsigned char)value; } return count; }\n"
        "static inline F2C_UNUSED size_t f2c_stream_write(const void *source, size_t size, "
        "f2c_io_stream *stream) { const unsigned char *bytes = (const unsigned char *)source; "
        "size_t count = 0U; if (stream != NULL && stream->kind == F2C_IO_STREAM_EXTERNAL) "
        "return stream->file != NULL ? fwrite(source, 1U, size, stream->file) : 0U; while (count "
        "< size && f2c_stream_putc(bytes[count], stream) != "
        "EOF) ++count; return count; }\n");
    f2c_buffer_append(
        output,
        "static inline F2C_UNUSED int f2c_stream_seek(f2c_io_stream *stream, long offset, int "
        "origin) { size_t base, target, distance; if (stream == NULL) return -1; if "
        "(stream->kind == F2C_IO_STREAM_EXTERNAL) return stream->file != NULL ? "
        "fseek(stream->file, offset, origin) : -1; base = origin == SEEK_SET ? 0U : origin == "
        "SEEK_CUR ? stream->position : origin == SEEK_END ? stream->virtual_size : SIZE_MAX; "
        "distance = offset < 0L ? (size_t)(-(offset + 1L)) + 1U : (size_t)offset; if (base == "
        "SIZE_MAX || (offset < 0L ? distance > base : distance > stream->virtual_size - base)) "
        "{ stream->error = true; return -1; } target = offset < 0L ? base - distance : base + "
        "distance; stream->position = target; stream->end_of_file = false; return 0; }\n"
        "static inline F2C_UNUSED long f2c_stream_tell(f2c_io_stream *stream) { if (stream == "
        "NULL) return -1L; if (stream->kind == F2C_IO_STREAM_EXTERNAL) return stream->file != "
        "NULL ? ftell(stream->file) : -1L; return stream->position <= (size_t)LONG_MAX ? "
        "(long)stream->position : -1L; }\n"
        "static inline F2C_UNUSED int f2c_stream_ungetc(int value, f2c_io_stream *stream) { "
        "if (stream == NULL || value == EOF) return EOF; if (stream->kind == "
        "F2C_IO_STREAM_EXTERNAL) return stream->file != NULL ? ungetc(value, stream->file) : "
        "EOF; if (stream->position == 0U) return EOF; --stream->position; "
        "stream->end_of_file = false; return value; }\n"
        "static inline F2C_UNUSED bool f2c_stream_eof(f2c_io_stream *stream) { return stream == "
        "NULL || (stream->kind == F2C_IO_STREAM_EXTERNAL ? stream->file == NULL || "
        "feof(stream->file) != 0 : stream->end_of_file); }\n"
        "static inline F2C_UNUSED bool f2c_stream_error(f2c_io_stream *stream) { return stream "
        "== NULL || (stream->kind == F2C_IO_STREAM_EXTERNAL ? stream->file == NULL || "
        "ferror(stream->file) != 0 : stream->error); }\n");
    f2c_buffer_append(
        output,
        "static inline F2C_UNUSED void f2c_stream_clear(f2c_io_stream *stream) { if (stream == "
        "NULL) return; stream->end_of_file = false; stream->error = false; if (stream->kind == "
        "F2C_IO_STREAM_EXTERNAL && stream->file != NULL) clearerr(stream->file); }\n"
        "static inline F2C_UNUSED int f2c_stream_flush(f2c_io_stream *stream) { if (stream == "
        "NULL) return EOF; return stream->kind == F2C_IO_STREAM_EXTERNAL ? (stream->file != "
        "NULL ? fflush(stream->file) : EOF) : (stream->error ? EOF : 0); }\n"
        "static inline F2C_UNUSED void f2c_stream_write_string(f2c_io_stream *stream, const "
        "char *value) { if (value != NULL) (void)f2c_stream_write(value, strlen(value), "
        "stream); }\n"
        "static inline F2C_UNUSED void f2c_stream_printf(f2c_io_stream *stream, const char "
        "*format, ...) { char local[128]; char *buffer = local; size_t capacity = "
        "sizeof(local); int count; va_list arguments; va_start(arguments, format); count = "
        "vsnprintf(local, capacity, format, arguments); va_end(arguments); if (count < 0) { if "
        "(stream != NULL) stream->error = true; return; } if ((size_t)count >= capacity) { "
        "capacity = (size_t)count + 1U; buffer = (char *)malloc(capacity); if (buffer == NULL) "
        "{ if (stream != NULL) stream->error = true; return; } va_start(arguments, format); "
        "count = vsnprintf(buffer, capacity, format, arguments); va_end(arguments); } if (count "
        "> 0) (void)f2c_stream_write(buffer, (size_t)count, stream); if (buffer != local) "
        "free(buffer); }\n");
}
