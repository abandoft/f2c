#include "core/generated/private.h"

void f2c_emit_numeric_operation_support(Buffer *output) {
    f2c_buffer_append(
        output,
        "#define F2C_DEFINE_NUMERIC_INTEGER(s, t, minimum, maximum) "
        "static inline F2C_UNUSED t f2c_dim_##s(t x, t y) { if (x <= y) return (t)0; if "
        "(y < 0 && x > (t)((maximum) + y)) abort(); return (t)(x - y); } "
        "static inline F2C_UNUSED t f2c_mod_##s(t a, t p) { if (p == 0) abort(); if (a == "
        "(minimum) && p == (t)-1) return (t)0; return (t)(a % p); } "
        "static inline F2C_UNUSED t f2c_modulo_##s(t a, t p) { t result = f2c_mod_##s(a, "
        "p); if (result != 0 && ((result < 0) != (p < 0))) result = (t)(result + p); return "
        "result; } "
        "static inline F2C_UNUSED t f2c_sign_##s(t a, t b) { t magnitude; if (a == "
        "(minimum)) { if (b < 0) return a; abort(); } magnitude = a < 0 ? (t)-a : a; return "
        "b < 0 ? (t)-magnitude : magnitude; }\n"
        "F2C_DEFINE_NUMERIC_INTEGER(i8, int8_t, INT8_MIN, INT8_MAX)\n"
        "F2C_DEFINE_NUMERIC_INTEGER(i16, int16_t, INT16_MIN, INT16_MAX)\n"
        "F2C_DEFINE_NUMERIC_INTEGER(i32, int32_t, INT32_MIN, INT32_MAX)\n"
        "F2C_DEFINE_NUMERIC_INTEGER(i64, int64_t, INT64_MIN, INT64_MAX)\n"
        "#undef F2C_DEFINE_NUMERIC_INTEGER\n"
        "#define F2C_DEFINE_NUMERIC_REAL(s, t, zero, absolute, remainder, copy_sign) "
        "static inline F2C_UNUSED t f2c_dim_##s(t x, t y) { return x > y ? x - y : "
        "(zero); } "
        "static inline F2C_UNUSED t f2c_mod_##s(t a, t p) { if (p == (zero)) abort(); return "
        "remainder(a, p); } "
        "static inline F2C_UNUSED t f2c_modulo_##s(t a, t p) { t result = f2c_mod_##s(a, "
        "p); if (result == (zero)) return copy_sign((zero), p); if ((result < (zero)) != (p "
        "< (zero))) result += p; return result; } "
        "static inline F2C_UNUSED t f2c_sign_##s(t a, t b) { return copy_sign(absolute(a), b); "
        "}\n"
        "F2C_DEFINE_NUMERIC_REAL(r4, float, 0.0f, fabsf, fmodf, copysignf)\n"
        "F2C_DEFINE_NUMERIC_REAL(r8, double, 0.0, fabs, fmod, copysign)\n"
        "#undef F2C_DEFINE_NUMERIC_REAL\n"
        "static inline F2C_UNUSED int64_t f2c_checked_integer_result(double value, int kind) "
        "{ double limit = kind == 1 ? 0x1p7 : kind == 2 ? 0x1p15 : kind == 4 ? 0x1p31 : "
        "kind == 8 ? 0x1p63 : 0.0; if (limit == 0.0 || !isfinite(value) || value < -limit || "
        "value >= limit) abort(); return (int64_t)value; }\n"
        "static inline F2C_UNUSED int64_t f2c_ceiling_integer(double value, int kind) { "
        "return f2c_checked_integer_result(ceil(value), kind); }\n"
        "static inline F2C_UNUSED int64_t f2c_floor_integer(double value, int kind) { return "
        "f2c_checked_integer_result(floor(value), kind); }\n"
        "static inline F2C_UNUSED int64_t f2c_nint_integer(double value, int kind) { return "
        "f2c_checked_integer_result(round(value), kind); }\n");
}
