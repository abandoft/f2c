#include "core/generated/private.h"

static void emit_unit_model(Buffer *output) {
    f2c_buffer_append(
        output,
        "typedef enum f2c_unit_access { F2C_ACCESS_SEQUENTIAL, F2C_ACCESS_DIRECT } "
        "f2c_unit_access;\n"
        "typedef enum f2c_unit_action { F2C_ACTION_READ, F2C_ACTION_WRITE, "
        "F2C_ACTION_READWRITE } f2c_unit_action;\n"
        "typedef enum f2c_unit_form { F2C_FORM_FORMATTED, F2C_FORM_UNFORMATTED } "
        "f2c_unit_form;\n"
        "typedef enum f2c_unit_blank { F2C_BLANK_NULL, F2C_BLANK_ZERO } f2c_unit_blank;\n"
        "typedef enum f2c_unit_delim { F2C_DELIM_NONE, F2C_DELIM_APOSTROPHE, "
        "F2C_DELIM_QUOTE } f2c_unit_delim;\n"
        "typedef struct f2c_unit_entry { int32_t unit; FILE *file; char *name; int32_t recl; "
        "f2c_unit_access access; f2c_unit_action action; f2c_unit_form form; "
        "f2c_unit_blank blank; f2c_unit_delim delim; bool pad; bool scratch; bool internal; "
        "struct f2c_unit_entry *next; } f2c_unit_entry;\n"
        "typedef struct f2c_inquiry { bool exist; bool opened; bool named; int32_t number; "
        "int32_t recl; int32_t nextrec; const char *name; const char *access; "
        "const char *sequential; const char *direct; const char *form; "
        "const char *formatted; const char *unformatted; const char *blank; "
        "const char *position; const char *action; const char *read; const char *write; "
        "const char *readwrite; const char *delim; const char *pad; char *owned_name; } "
        "f2c_inquiry;\n"
        "static _Thread_local f2c_unit_entry *f2c_unit_list;\n"
        "static _Thread_local int32_t f2c_internal_unit_next = -1;\n"
        "static _Thread_local unsigned f2c_child_io_depth;\n");
}

static void emit_unit_utilities(Buffer *output) {
    f2c_buffer_append(
        output,
        "static inline F2C_UNUSED f2c_unit_entry *f2c_find_unit(int32_t unit) { "
        "f2c_unit_entry *entry; for (entry = f2c_unit_list; entry != NULL; entry = "
        "entry->next) if (entry->unit == unit) return entry; return NULL; }\n"
        "static inline F2C_UNUSED char *f2c_trimmed_copy(const char *value, size_t length) { "
        "char *copy; while (length != 0U && value[length - 1U] == ' ') --length; copy = "
        "(char *)malloc(length + 1U); if (copy == NULL) return NULL; if (length != 0U) "
        "memcpy(copy, value, length); copy[length] = '\\0'; return copy; }\n"
        "static inline F2C_UNUSED bool f2c_option_equal(const char *value, size_t length, "
        "const char *expected) { size_t begin = 0U, index = 0U; while (begin < length && "
        "value[begin] == ' ') ++begin; while (length > begin && value[length - 1U] == ' ') "
        "--length; while (begin + index < length && expected[index] != '\\0') { if "
        "(tolower((unsigned char)value[begin + index]) != "
        "tolower((unsigned char)expected[index])) return false; ++index; } return begin + "
        "index == length && expected[index] == '\\0'; }\n"
        "static inline F2C_UNUSED bool f2c_empty_option(const char *value, size_t length) { "
        "size_t index; for (index = 0U; index < length; ++index) if (value[index] != ' ') "
        "return false; return true; }\n"
        "static inline F2C_UNUSED bool f2c_file_exists(const char *name) { FILE *probe = "
        "fopen(name, \"rb\"); if (probe == NULL) return false; (void)fclose(probe); return "
        "true; }\n"
        "static inline F2C_UNUSED char *f2c_default_unit_name(int32_t unit) { char buffer[64]; "
        "int length = snprintf(buffer, sizeof(buffer), \"fort.%d\", (int)unit); if (length < "
        "0 || (size_t)length >= sizeof(buffer)) return NULL; return f2c_trimmed_copy(buffer, "
        "(size_t)length); }\n"
        "static inline F2C_UNUSED void f2c_free_unit_entry(f2c_unit_entry *entry) { if (entry "
        "!= NULL) { free(entry->name); free(entry); } }\n"
        "static inline F2C_UNUSED void f2c_assign_inquiry_character(char *destination, size_t "
        "length, const char *source) { size_t count = source != NULL ? strlen(source) : 0U; "
        "if (count > length) count = length; if (count != 0U) memmove(destination, source, "
        "count); if (length > count) memset(destination + count, ' ', length - count); }\n");
}

static void emit_unit_open_close(Buffer *output) {
    f2c_buffer_append(
        output,
        "static inline F2C_UNUSED bool f2c_close_unit_with_status(int32_t unit, const char "
        "*status, size_t status_length) { f2c_unit_entry **link = &f2c_unit_list; bool "
        "delete_file; while (*link != NULL && (*link)->unit != unit) link = &(*link)->next; "
        "if (!f2c_option_equal(status, status_length, \"keep\") && "
        "!f2c_option_equal(status, status_length, \"delete\")) return false; delete_file = "
        "f2c_option_equal(status, status_length, \"delete\"); if (*link == NULL) { if "
        "(delete_file) return false; if (unit == 0) return fflush(stderr) == 0; if (unit == 6) "
        "return fflush(stdout) == 0; return true; } { f2c_unit_entry *entry = *link; bool ok; "
        "if (entry->internal) return false; *link = entry->next; ok = fclose(entry->file) == "
        "0; if (delete_file && !entry->scratch) { if (entry->name == NULL || "
        "remove(entry->name) != 0) ok = false; } f2c_free_unit_entry(entry); return ok; } }\n"
        "static inline F2C_UNUSED bool f2c_close_unit(int32_t unit) { return "
        "f2c_close_unit_with_status(unit, \"keep\", 4U); }\n");
    f2c_buffer_append(
        output,
        "static inline F2C_UNUSED bool f2c_open_unit_full(int32_t unit, const char *path, "
        "size_t path_length, const char *status, size_t status_length, const char *access, "
        "size_t access_length, const char *action, size_t action_length, const char *form, "
        "size_t form_length, int32_t recl, const char *blank, size_t blank_length, const char "
        "*position, size_t position_length, const char *delim, size_t delim_length, const char "
        "*pad, size_t pad_length) { f2c_unit_entry *entry; FILE *file = NULL; char *name = "
        "NULL; bool exists = false; bool scratch; f2c_unit_access access_value; "
        "f2c_unit_action action_value; f2c_unit_form form_value; f2c_unit_blank blank_value; "
        "f2c_unit_delim delim_value; bool pad_value; bool append; bool rewind_position; if "
        "(unit < 0) return false; scratch = f2c_option_equal(status, status_length, "
        "\"scratch\"); if (!scratch && !f2c_option_equal(status, status_length, \"old\") && "
        "!f2c_option_equal(status, status_length, \"new\") && "
        "!f2c_option_equal(status, status_length, \"replace\") && "
        "!f2c_option_equal(status, status_length, \"unknown\")) return false; if (scratch && "
        "!f2c_empty_option(path, path_length)) return false; if "
        "(f2c_option_equal(access, access_length, \"sequential\")) access_value = "
        "F2C_ACCESS_SEQUENTIAL; else if (f2c_option_equal(access, access_length, \"direct\")) "
        "access_value = F2C_ACCESS_DIRECT; else return false; if (access_value == "
        "F2C_ACCESS_DIRECT ? recl <= 0 : recl != 0) return false; if "
        "(f2c_option_equal(action, action_length, \"read\")) action_value = F2C_ACTION_READ; "
        "else if (f2c_option_equal(action, action_length, \"write\")) action_value = "
        "F2C_ACTION_WRITE; else if (f2c_option_equal(action, action_length, \"readwrite\")) "
        "action_value = F2C_ACTION_READWRITE; else return false; if "
        "(f2c_empty_option(form, form_length)) form_value = access_value == "
        "F2C_ACCESS_DIRECT ? F2C_FORM_UNFORMATTED : F2C_FORM_FORMATTED; else if "
        "(f2c_option_equal(form, form_length, \"formatted\")) form_value = "
        "F2C_FORM_FORMATTED; else if (f2c_option_equal(form, form_length, \"unformatted\")) "
        "form_value = F2C_FORM_UNFORMATTED; else return false; if "
        "(f2c_option_equal(blank, blank_length, \"null\")) blank_value = F2C_BLANK_NULL; else "
        "if (f2c_option_equal(blank, blank_length, \"zero\")) blank_value = F2C_BLANK_ZERO; "
        "else return false; append = f2c_option_equal(position, position_length, \"append\"); "
        "rewind_position = f2c_option_equal(position, position_length, \"rewind\"); if "
        "(!append && !rewind_position && !f2c_option_equal(position, position_length, "
        "\"asis\")) return false; if (f2c_option_equal(delim, delim_length, \"none\")) "
        "delim_value = F2C_DELIM_NONE; else if (f2c_option_equal(delim, delim_length, "
        "\"apostrophe\")) delim_value = F2C_DELIM_APOSTROPHE; else if "
        "(f2c_option_equal(delim, delim_length, \"quote\")) delim_value = F2C_DELIM_QUOTE; "
        "else return false; if (f2c_option_equal(pad, pad_length, \"yes\")) pad_value = true; "
        "else if (f2c_option_equal(pad, pad_length, \"no\")) pad_value = false; else return "
        "false; ");
    f2c_buffer_append(
        output,
        "if (scratch) { if (!f2c_close_unit(unit)) return false; file = tmpfile(); } else { name = "
        "f2c_empty_option(path, path_length) ? f2c_default_unit_name(unit) : "
        "f2c_trimmed_copy(path, path_length); if (name == NULL) return false; exists = "
        "f2c_file_exists(name); if (f2c_option_equal(status, status_length, \"old\") && "
        "!exists) { free(name); return false; } if (f2c_option_equal(status, status_length, "
        "\"new\") && exists) { free(name); return false; } if (action_value == "
        "F2C_ACTION_READ && (!exists || f2c_option_equal(status, status_length, \"replace\") || "
        "f2c_option_equal(status, status_length, \"new\"))) { free(name); return false; } "
        "if (!f2c_close_unit(unit)) { free(name); return false; } if "
        "(f2c_option_equal(status, status_length, \"replace\") || !exists) file = "
        "fopen(name, \"wb+\"); else file = fopen(name, action_value == F2C_ACTION_READ ? "
        "\"rb\" : \"rb+\"); } if (file == NULL) { free(name); return false; } if ((append "
        "&& fseek(file, 0L, SEEK_END) != 0) || (rewind_position && fseek(file, 0L, SEEK_SET) "
        "!= 0)) { (void)fclose(file); free(name); return false; } entry = (f2c_unit_entry "
        "*)malloc(sizeof(*entry)); if (entry == NULL) { (void)fclose(file); free(name); return "
        "false; } entry->unit = unit; entry->file = file; entry->name = name; entry->recl = "
        "recl; entry->access = access_value; entry->action = action_value; entry->form = "
        "form_value; entry->blank = blank_value; entry->delim = delim_value; entry->pad = "
        "pad_value; entry->scratch = scratch; entry->internal = false; entry->next = "
        "f2c_unit_list; f2c_unit_list = entry; return true; }\n"
        "static inline F2C_UNUSED bool f2c_open_unit(int32_t unit, const char *path, size_t "
        "path_length, const char *status, size_t status_length, const char *form, size_t "
        "form_length) { return f2c_open_unit_full(unit, path, path_length, status, "
        "status_length, \"sequential\", 10U, \"readwrite\", 9U, form, form_length, 0, "
        "\"null\", 4U, \"asis\", 4U, \"none\", 4U, \"yes\", 3U); }\n");
}

static void emit_unit_connection(Buffer *output) {
    f2c_buffer_append(
        output,
        "static inline F2C_UNUSED FILE *f2c_unit_file(int32_t unit, bool input) { "
        "f2c_unit_entry *entry = f2c_find_unit(unit); if (entry != NULL) { if ((input && "
        "entry->action == F2C_ACTION_WRITE) || (!input && entry->action == F2C_ACTION_READ)) "
        "return NULL; return entry->file; } if (input && unit == 5) return stdin; if (!input && "
        "unit == 6) return stdout; if (!input && unit == 0) return stderr; if (unit < 0) return "
        "NULL; if (!f2c_open_unit_full(unit, \"\", 0U, \"unknown\", 7U, \"sequential\", "
        "10U, input ? \"read\" : \"write\", input ? 4U : 5U, \"formatted\", 9U, 0, "
        "\"null\", 4U, \"asis\", 4U, \"none\", 4U, \"yes\", 3U)) return NULL; entry = "
        "f2c_find_unit(unit); return entry != NULL ? entry->file : NULL; }\n"
        "static inline F2C_UNUSED int32_t f2c_register_internal_unit(FILE *file) { "
        "f2c_unit_entry *entry = (f2c_unit_entry *)calloc(1U, sizeof(*entry)); int32_t unit; "
        "if (entry == NULL) abort(); do { unit = f2c_internal_unit_next; "
        "f2c_internal_unit_next = unit == INT32_MIN ? -1 : unit - 1; } while "
        "(f2c_find_unit(unit) != NULL); entry->unit = unit; entry->file = file; entry->access = "
        "F2C_ACCESS_SEQUENTIAL; entry->action = F2C_ACTION_READWRITE; entry->form = "
        "F2C_FORM_FORMATTED; entry->blank = F2C_BLANK_NULL; entry->delim = F2C_DELIM_NONE; "
        "entry->pad = true; entry->internal = true; entry->next = f2c_unit_list; f2c_unit_list "
        "= entry; return unit; }\n"
        "static inline F2C_UNUSED void f2c_unregister_internal_unit(int32_t unit) { "
        "f2c_unit_entry **link = &f2c_unit_list; while (*link != NULL && (*link)->unit != unit) "
        "link = &(*link)->next; if (*link != NULL) { f2c_unit_entry *entry = *link; *link = "
        "entry->next; f2c_free_unit_entry(entry); } }\n");
}

static void emit_positioning(Buffer *output) {
    f2c_buffer_append(
        output,
        "static inline F2C_UNUSED bool f2c_rewind_unit(int32_t unit) { f2c_unit_entry *entry = "
        "f2c_find_unit(unit); FILE *file; if (entry != NULL && entry->access != "
        "F2C_ACCESS_SEQUENTIAL) return false; file = entry != NULL ? entry->file : "
        "f2c_unit_file(unit, false); if (file == "
        "NULL) return false; clearerr(file); return fseek(file, 0L, SEEK_SET) == 0; }\n"
        "static inline F2C_UNUSED bool f2c_backspace_unit(int32_t unit) { f2c_unit_entry *entry "
        "= f2c_find_unit(unit); FILE *file; long cursor; int character; if (entry != NULL && "
        "entry->access != F2C_ACCESS_SEQUENTIAL) return false; file = entry != NULL ? "
        "entry->file : f2c_unit_file(unit, false); if (file == NULL) return false; "
        "clearerr(file); cursor = ftell(file); if "
        "(cursor < 0L) return false; if (cursor == 0L) return true; --cursor; if (fseek(file, "
        "cursor, SEEK_SET) != 0) return false; character = fgetc(file); if (character == "
        "'\\n' && cursor != 0L) --cursor; for (;;) { if (fseek(file, cursor, SEEK_SET) != 0) "
        "return false; character = fgetc(file); if (character == '\\n') { ++cursor; break; } "
        "if (cursor == 0L) break; --cursor; } clearerr(file); return fseek(file, cursor, "
        "SEEK_SET) == 0; }\n"
        "static inline F2C_UNUSED bool f2c_copy_file_prefix(FILE *source, FILE *destination, "
        "long length) { unsigned char buffer[4096]; long remaining = length; while (remaining "
        "> 0L) { size_t requested = remaining < (long)sizeof(buffer) ? (size_t)remaining : "
        "sizeof(buffer); size_t count = fread(buffer, 1U, requested, source); if (count != "
        "requested || fwrite(buffer, 1U, count, destination) != count) return false; remaining "
        "-= (long)count; } return true; }\n"
        "static inline F2C_UNUSED bool f2c_endfile_unit(int32_t unit) { f2c_unit_entry *entry = "
        "f2c_find_unit(unit); FILE *snapshot; FILE *replacement; long position; bool copied; "
        "bool closed = true; if "
        "(entry == NULL || entry->internal || entry->access != F2C_ACCESS_SEQUENTIAL || "
        "entry->action == F2C_ACTION_READ) return false; position = ftell(entry->file); if "
        "(position < 0L || fseek(entry->file, position, SEEK_SET) != 0) return false; snapshot "
        "= tmpfile(); if (snapshot == NULL || fseek(entry->file, 0L, SEEK_SET) != 0 || "
        "!f2c_copy_file_prefix(entry->file, snapshot, position) || fseek(snapshot, 0L, SEEK_SET) "
        "!= 0) { if (snapshot != NULL) (void)fclose(snapshot); return false; } replacement = "
        "entry->scratch ? tmpfile() : NULL; if (!entry->scratch) { closed = "
        "fclose(entry->file) == 0; entry->file = NULL; replacement = "
        "fopen(entry->name, \"wb+\"); if (replacement == NULL) entry->file = "
        "fopen(entry->name, \"rb+\"); } if (replacement == NULL) { (void)fclose(snapshot); "
        "return false; } copied = f2c_copy_file_prefix(snapshot, replacement, position); "
        "(void)fclose(snapshot); if (entry->scratch) (void)fclose(entry->file); entry->file = "
        "replacement; if (!copied || fseek(entry->file, position, SEEK_SET) != 0) return false; "
        "return closed; }\n");
}

static void emit_inquiry(Buffer *output) {
    f2c_buffer_append(
        output,
        "static inline F2C_UNUSED void f2c_inquiry_initialize(f2c_inquiry *result) { "
        "memset(result, 0, sizeof(*result)); result->number = -1; result->name = \"\"; "
        "result->access = \"UNDEFINED\"; result->sequential = \"UNKNOWN\"; result->direct = "
        "\"UNKNOWN\"; result->form = \"UNDEFINED\"; result->formatted = \"UNKNOWN\"; "
        "result->unformatted = \"UNKNOWN\"; result->blank = \"UNDEFINED\"; "
        "result->position = \"UNDEFINED\"; result->action = \"UNDEFINED\"; result->read = "
        "\"UNKNOWN\"; result->write = \"UNKNOWN\"; result->readwrite = \"UNKNOWN\"; "
        "result->delim = \"UNDEFINED\"; result->pad = \"UNDEFINED\"; }\n"
        "static inline F2C_UNUSED const char *f2c_unit_position(f2c_unit_entry *entry) { long "
        "current, end; if (entry->access == F2C_ACCESS_DIRECT) return \"UNDEFINED\"; current "
        "= ftell(entry->file); if (current < 0L) return \"UNKNOWN\"; if (fseek(entry->file, "
        "0L, SEEK_END) != 0) return \"UNKNOWN\"; end = ftell(entry->file); if "
        "(fseek(entry->file, current, SEEK_SET) != 0 || end < 0L) return \"UNKNOWN\"; if "
        "(current == 0L) return \"REWIND\"; return current == end ? \"APPEND\" : \"ASIS\"; "
        "}\n");
    f2c_buffer_append(
        output,
        "static inline F2C_UNUSED void f2c_fill_inquiry(f2c_unit_entry *entry, f2c_inquiry "
        "*result) { long position; result->exist = true; result->opened = true; result->number "
        "= entry->unit; result->named = entry->name != NULL && !entry->scratch; result->name = "
        "result->named ? entry->name : \"\"; result->access = entry->access == "
        "F2C_ACCESS_SEQUENTIAL ? \"SEQUENTIAL\" : \"DIRECT\"; result->sequential = "
        "entry->access == F2C_ACCESS_SEQUENTIAL ? \"YES\" : \"NO\"; result->direct = "
        "entry->access == F2C_ACCESS_DIRECT ? \"YES\" : \"NO\"; result->form = entry->form "
        "== F2C_FORM_FORMATTED ? \"FORMATTED\" : \"UNFORMATTED\"; result->formatted = "
        "entry->form == F2C_FORM_FORMATTED ? \"YES\" : \"NO\"; result->unformatted = "
        "entry->form == F2C_FORM_UNFORMATTED ? \"YES\" : \"NO\"; result->recl = "
        "entry->recl; position = ftell(entry->file); result->nextrec = entry->access == "
        "F2C_ACCESS_DIRECT && position >= 0L ? (int32_t)(position / entry->recl + 1L) : 0; "
        "result->blank = entry->blank == F2C_BLANK_ZERO ? \"ZERO\" : \"NULL\"; "
        "result->position = f2c_unit_position(entry); result->action = entry->action == "
        "F2C_ACTION_READ ? \"READ\" : (entry->action == F2C_ACTION_WRITE ? \"WRITE\" : "
        "\"READWRITE\"); result->read = entry->action == F2C_ACTION_WRITE ? \"NO\" : "
        "\"YES\"; result->write = entry->action == F2C_ACTION_READ ? \"NO\" : \"YES\"; "
        "result->readwrite = entry->action == F2C_ACTION_READWRITE ? \"YES\" : \"NO\"; "
        "result->delim = entry->delim == F2C_DELIM_APOSTROPHE ? \"APOSTROPHE\" : "
        "(entry->delim == F2C_DELIM_QUOTE ? \"QUOTE\" : \"NONE\"); result->pad = "
        "entry->pad ? \"YES\" : \"NO\"; }\n");
    f2c_buffer_append(
        output,
        "static inline F2C_UNUSED void f2c_fill_preconnected_inquiry(int32_t unit, "
        "f2c_inquiry *result) { result->exist = true; result->opened = true; result->number = "
        "unit; result->access = \"SEQUENTIAL\"; result->sequential = \"YES\"; result->direct "
        "= \"NO\"; result->form = \"FORMATTED\"; result->formatted = \"YES\"; "
        "result->unformatted = \"NO\"; result->blank = \"NULL\"; result->position = "
        "\"UNKNOWN\"; result->action = unit == 5 ? \"READ\" : \"WRITE\"; result->read = "
        "unit == 5 ? \"YES\" : \"NO\"; result->write = unit == 5 ? \"NO\" : \"YES\"; "
        "result->readwrite = \"NO\"; result->delim = \"NONE\"; result->pad = \"YES\"; }\n"
        "static inline F2C_UNUSED bool f2c_inquire_unit(int32_t unit, f2c_inquiry *result) { "
        "f2c_unit_entry *entry; if (result == NULL) return false; f2c_inquiry_initialize(result); "
        "entry = f2c_find_unit(unit); if (entry != NULL) f2c_fill_inquiry(entry, result); else "
        "if (unit == 0 || unit == 5 || unit == 6) f2c_fill_preconnected_inquiry(unit, result); "
        "else result->exist = unit >= 0; return true; }\n"
        "static inline F2C_UNUSED bool f2c_inquire_file(const char *path, size_t path_length, "
        "f2c_inquiry *result) { f2c_unit_entry *entry; char *name; if (result == NULL) return "
        "false; f2c_inquiry_initialize(result); name = f2c_trimmed_copy(path, path_length); if "
        "(name == NULL) return false; result->owned_name = name; result->name = name; "
        "result->named = name[0] != '\\0'; result->exist = name[0] != '\\0' && "
        "f2c_file_exists(name); for (entry = f2c_unit_list; entry != NULL; entry = entry->next) "
        "if (entry->name != NULL && strcmp(entry->name, name) == 0) { char *owned = "
        "result->owned_name; f2c_fill_inquiry(entry, result); result->owned_name = owned; "
        "result->name = owned; break; } return true; }\n"
        "static inline F2C_UNUSED void f2c_inquiry_dispose(f2c_inquiry *result) { if (result != "
        "NULL) { free(result->owned_name); result->owned_name = NULL; } }\n");
}

void f2c_emit_file_unit_support(Buffer *output) {
    emit_unit_model(output);
    emit_unit_utilities(output);
    emit_unit_open_close(output);
    emit_unit_connection(output);
    emit_positioning(output);
    emit_inquiry(output);
}
